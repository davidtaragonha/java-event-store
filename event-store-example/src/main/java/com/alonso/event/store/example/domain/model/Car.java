package com.alonso.event.store.example.domain.model;

public class Car {
    private String id;
    private String company;
    private String model;
    private double price;

    public String getId() {
        return id;
    }

    public String getCompany() {
        return company;
    }

    public String getModel() {
        return model;
    }

    public double getPrice() {
        return price;
    }
}
